
package prasad_java_assignements.Country;
import java.util.*;

public class Country {
    String code;
    String name;

    public Country(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String toString() {
        return "Country{code='" + code + "', name='" + name + "'}";
    }
}




