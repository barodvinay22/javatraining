package AssignmentSep17;
class Calculator {
    // Method to add 2 numbers
    public int add(int a, int b) {
        return a + b;
    }

    // Method to add 3 numbers
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    // Method to add 4 numbers
    public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
